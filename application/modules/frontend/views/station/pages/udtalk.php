<script>
function saveUDImage() {
	ajaxindicatorstart("Please wait .. while we save item...");
	var options = {
			target : '#response', // target element(s) to be updated with server response 
			beforeSubmit : showUDRequest, // pre-submit callback 
			success :  showUDResponse,
			url : base_url+'mystation/udtalks/saveimage',
			semantic : true,
			dataType : 'json'
		};
	$('#udtalkupload').ajaxSubmit(options);
}
function showUDRequest(formData, jqForm, options){
	var queryString = $.param(formData);
	if($("#FileUpload3").val() != "") {
		var file = $("#FileUpload3")[0].files[0];
		var ext = file.name.split('.').pop();
		var value = $("#FileUpload3").val();
		if(ext == 'JPG' || ext == 'JPEG' || ext == 'jpg' || ext == 'jpeg' || ext == 'png' || ext == 'bmp' || ext == 'gif' ) {
			return true;
		} else {
			ajaxindicatorstop();
			alert("Please upload images only."); 
			$("#FileUpload3").val("");
			return false;
		}
	} else {
		ajaxindicatorstop();
		alert("Select file first.");
		return false;
	}
}
function showUDResponse(resp, statusText, xhr, $form){
	ajaxindicatorstop();
	if(resp.status == 0) {
		alert("Failed to upload image.");
	} else {
		$.get(base_url+"mystation/udtalk",{},function(data){
			$("#Layer338_Container").html(data);
		});
	}
}
function deleteUDImage(id) {
	ajaxindicatorstart("Please wait .. while we save item...");
	$.get(base_url+"mystation/udtalk/delete/"+id,{},function(data){
		$.get(base_url+"mystation/udtalk",{},function(data){
			$("#Layer338_Container").html(data);
		});
		ajaxindicatorstop();
	},'json');
}
